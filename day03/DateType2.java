/*
      八种数据类型的默认值（在成员变量中没赋值系统会自动赋值）
	  数据类型                     默认值
	-------------------------------------------------
	  byte.int.short.long          0
	  double  float                   0.0
	  boolean                          false(0)
	  char                                \u0000
*/

public class DateType2
{

	static int k;    //只有成员变量才会自动赋值
	public static void main(String[ ] args)
	{
		//int i;   局部变量不会自动赋值，所以会出错    
		//System.out.println(i);
		System.out.println(k);  //输出结果是0
	}
}